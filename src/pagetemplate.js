//function to take in roles and call appropriate function to generate cards that will go into html
generateHTML = (data) => {

    // array for cards 
    pageArray = []; 

    for (let i = 0; i < data.length; i++) {
        const employee = data[i];
        const role = employee.getRole(); 


        // call manager function
        if (role === 'Manager') {
            const managerCard = manager(employee);

            pageArray.push(managerCard);
        }

        // call engineer function
        if (role === 'Engineer') {
            const engineerCard = engineer(employee);

            pageArray.push(engineerCard);
        }

        // call intern function 
        if (role === 'Intern') {
            const internCard = intern(employee);

            pageArray.push(internCard);
        }
        
    }

    // joining strings 
    const staffDiv = pageArray.join('')

   //generate page 
    const generateTeam = template(staffDiv); 
    return generateTeam;

}
